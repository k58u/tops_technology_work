"""
Object-Oriented Programming (OOP) is based on several key concepts:

Encapsulation: Bundling data and methods that operate on the data into a single 
unit, or class. It restricts direct access to some of an object's components.

Inheritance: A mechanism where a new class (subclass) derives properties and 
behaviors from an existing class (superclass).

Polymorphism: The ability for different classes to be treated as instances of 
the same class through a common interface. It allows methods to do different things 
based on the object it is acting upon.

Abstraction: Hiding complex implementation details and showing only the necessary 
features of an object. It helps in reducing complexity by focusing on interactions at a high level.

Multiple Inheritance in Java:

Java does not support multiple inheritance (where a class can 
inherit from more than one class) directly. This is to avoid complexity and ambiguity.
Instead, Java uses interfaces to achieve a similar effect. A class can implement multiple interfaces, 
allowing it to inherit behaviors from multiple sources without the issues of multiple inheritance.
"""